package sync.dto;

import javax.persistence.Embeddable;
import javax.persistence.Column;
import java.io.Serializable;

@Embeddable
public class TicketLine implements Serializable {
        @Column(name="ticket")
        private String ticket;

        @Column(name="line")
        private int line;

    public String getTicket() {
        return ticket;
    }
}
